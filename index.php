            document.addEventListener('DOMContentLoaded', function () {
            // Ajouter un gestionnaire d'événements de défilement
            var aboutSectionVisible = false;

            window.addEventListener('scroll', function () {
                // Vérifier si la section "About" est visible à l'écran
                var aboutSection = document.getElementById('about');
                var rect = aboutSection.getBoundingClientRect();

                // Si la section "About" est visible à l'écran et n'a pas été traitée, appeler la fonction typewriter
                if (rect.top <= window.innerHeight && rect.bottom >= 0 && !aboutSectionVisible) {
                    aboutSectionVisible = true; // Marquer la section comme traitée
                    typeWriter();
                } else if (rect.top > window.innerHeight || rect.bottom < 0) {
                    // Si la section n'est pas visible, réinitialiser le texte et la variable
                    aboutSectionVisible = false;
                    resetTypewriter();
                }
            });
        });

        var i = 0;
        var txt = "Qu'est-ce que TonationRP?"; /* Le texte que vous souhaitez animer */
        var speed = 100; /* La vitesse/durée de l'effet en millisecondes */

        function typeWriter() {
            var demoElement = document.getElementById("demo");
            if (i < txt.length) {
                // Ajouter le caractère au texte et souligner le texte
                demoElement.innerHTML += '<u>' + txt.charAt(i) + '</u>';
                i++;
                setTimeout(typeWriter, speed);
            }
        }

        function resetTypewriter() {
            // Réinitialiser le texte et l'index
            var demoElement = document.getElementById("demo");
            demoElement.innerHTML = "";
            i = 0;
        }
