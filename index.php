                          <div class="featured-text text-center text-lg-left">
                            <h4 class="text-white-center"><u>Le RP en général:</u></h4><br />
                          </div>
                          <p class="text-white-gauche">
<u>Bunny Hopping:</u> C'est le fait de sauter sans cesse pour aller plus vite ==> Strictement interdit<br /><br />

<u>ForceRP:</u> C'est le fait d'obliger quelqu'un à jouer un rôle ou le fait de forcer une action RP pour aboutir à une fin.<br /><br />

<u>Fear:</u> C'est le fait de réagir face à la peur (utilisez le /me).<br /><br />

<u>MassRP:</u> Quand vous faites une action, peu importe son type, vous devez vous dire qu'en ville, il n'y a pas que vous et qu'il y a pleins d'autres civils. Par conséquent tout ce que vous faites est vu par les civils et il y aura des témoins.<br /><br />

<u>PowerGaming:</u> Fait de réaliser des actions irréalisables dans la vraie vie.<br /><br />

<u>MetaGaming:</u> C'est le fait de profiter d'une faille du jeu pour le RP. Par exemple, si vous voyez le pseudo de quelqu'un au dessus de sa tête, vous n'êtes pas censé savoir son identité.<br /><br />

<u>Freekill:</u> Fait de tuer tout le monde sans aucune raison.<br /><br />

<u>PainRP:</u> Fait de jouer le pain, c'est-à-dire de ressentir la douleur. Dans le but de favoriser un minimum le rôle des médecins sur le serveur, les chutes graves devront obligatoirement être accompagnés d'une visite chez un médecin obligatoirement. <br /><br />

<u>Troll:</u> Il est interdit de troller, de se montrer vexant ou insolent envers les autres joueurs.<br /><br />

<u>Grief:</u> C'est le fait de détruite un véhicule ou autre, appartenant à un joueur hors actionRP.<br /><br />
                        </p>
